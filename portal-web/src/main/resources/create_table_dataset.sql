CREATE TABLE IF NOT EXISTS `dataset`(
	`id` INT UNSIGNED AUTO_INCREMENT COMMENT 'INDEX ID',
	`dataset_name` VARCHAR(200) UNIQUE NOT NULL COMMENT '数据集名称',
	`product` VARCHAR(100) NOT NULL COMMENT '产品线',
	`create_time` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建日期',
	`update_time` TIMESTAMP NOT NULL ON UPDATE CURRENT_TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '更新日期',
	`store_period` INT UNSIGNED NOT NULL COMMENT '保存时间(天)',
	`size` FLOAT UNSIGNED NOT NULL COMMENT '预估数据量大小(G/天)',
	`contact_person` VARCHAR(200) NOT NULL COMMENT '联系人(邮箱前缀逗号隔)',
	`comment` TEXT COMMENT '备注',
	PRIMARY KEY (`id`)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;